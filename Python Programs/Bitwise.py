# 1. Complement ( ~ -->  Complement)
# 2. And ( & ) , Or ( | )  , XOR ( ^ )
# 3. Left Shift ( << ) Right Shift ( >> )
print(~17)
print(~25)
print(~41)
print(~13)
print("\n\n")


print(12 & 13)

print(12 | 13)

print(28 | 15)

print(25 | 30)

print("\n\n")


print(25 ^ 30)
print(28 ^ 15)
print("\n\n")

print(10 << 2)
print(10 >> 2)